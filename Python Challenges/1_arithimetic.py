#  Write a program to demonstrate the usage of various arithmetic operators.

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print(f"Addition: {a} + {b} = {a + b}")
print(f"Subtraction: {a} - {b} = {a - b}")
print(f"Multiplication: {a} * {b} = {a * b}")
print(f"Division: {a} / {b} = {a / b}")  # Gives float value
print(f"Floor Division: {a} // {b} = {a // b}")  # Removes decimal part
print(f"Modulus: {a} % {b} = {a % b}")  # Gives remainder
print(f"Exponentiation: {a} ** {b} = {a ** b}")  # Power operation
